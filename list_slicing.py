numbers=[x for x in range(0,1000) if x%100==0]
print(numbers)
#Using List SLicing here
print(numbers[0:10])
#Slicing The List
print(numbers[4:5])
#From STarting To The Paticlar Position
print(numbers[:6])
#Slicing from an index to the end
print(numbers[7:])
